int sl(string s) {
  int tl = 0; for(char &c : s) tl += c - '0';
  return tl;
}

void solve() {
  int n; cin >> n;
  vector<string> a(n);
  for(string &x : a) cin >> x;
  int ans = 0;
  for(string s : a) {
    for(string x : a){
      string g = s + x;
      int gl = g.length();
      if(g.length() % 2 == 0 && sl(g.substr(0, gl / 2)) == sl(g.substr(gl / 2, gl))) {
        ans++;
        printf("%s+%s\n", s.c_str(), x.c_str());
      }
    }
  }
  cout << ans;
}

int main() {
  solve();
  return 0;
}

